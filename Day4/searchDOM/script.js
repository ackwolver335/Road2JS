// Changing the card title color to one
// Getting the card
let cardData = document.getElementsByClassName("card-title")[0];

cardData.style.color = "red";

/*
  getElementById : Used for getting the element by the id but the properties are uniquely identified.
  getElementByClassName : Used for applying the properties to a single class.
  getElementsByClassName : Used for applying the properties to all the class using that particular classname.
  querySelector : Used for applying properties or query to id and class at single basis.
  querySelectorAll : Used for applying properties or query to id and classes basis.
  getElementByTagName : Used for getting element using the tag name.
  getElementByName : Used for getting the name using its value.
*/